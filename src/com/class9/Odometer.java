package com.class9;

public class Odometer {

	public static void main(String[] args) {
		// Odometer
		
		for(int i=0; i<=9; i++) {
			for (int j=0; j<=9; j++) {
				for(int k=0; k<=9; k++) {
					for(int m=0; m<=9; m++) {
						System.out.println(i+" "+j+" "+k+" "+m);
					}
				}
			}
		}

	}

}
